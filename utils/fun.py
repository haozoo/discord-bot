def coder(string):
    dict = {'Q': '%', 'W': '^', 'E': '~', 'R': '|', 'T': '[',
            'Y': ']', 'U': '<', 'I': '>', 'O': '{', 'P': '}',
            'A': '@', 'S': '#', 'D': '&', 'F': '*', 'G': '-',
            'H': '+', 'J': '=', 'K': '(', 'L': ')', 'Z': '_',
            'X': '$', 'C': '"', 'V': '\'', 'B': ':', 'N': ';',
            'M': '/',
            '1': ',', '2': '.', '3': '!', '4': '?',
            '%': 'Q', '^': 'W', '~': 'E', '|': 'R', '[': 'T',
            ']': 'Y', '<': 'U', '>': 'I', '{': 'O', '}': 'P',
            '@': 'A', '#': 'S', '&': 'D', '*': 'F', '-': 'G',
            '+': 'H', '=': 'J', '(': 'K', ')': 'L', '_': 'Z',
            '$': 'X', '"': 'C', '\'': 'V', ':': 'B', ';': 'N',
            '/': 'M',
            ',': '1', '.': '2', '!': '3', '?': '4',
            ' ': '  '}

    string = string.upper()
    message = ''

    for letter in string:
        if letter in dict:
            message = message + dict[letter]
        else:
            message = message + 'x'

    return message
